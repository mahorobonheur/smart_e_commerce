package org.example.smartecommercesystem.model;

import java.sql.Timestamp;

public class Review {
    private String reviewId;
    private Integer sqlId;
    private int productId;
    private int userId;
    private int rating;
    private String comment;
    private Timestamp createdAt;

    public Review() {}

    public Review(String reviewId, Integer sqlId, int productId, int userId, int rating, String comment, Timestamp createdAt) {
        this.reviewId = reviewId;
        this.sqlId = sqlId;
        this.productId = productId;
        this.userId = userId;
        this.rating = rating;
        this.comment = comment;
        this.createdAt = createdAt;
    }

    // Getters & Setters
    public String getReviewId() { return reviewId; }
    public void setReviewId(String reviewId) { this.reviewId = reviewId; }

    public Integer getSqlId() { return sqlId; }
    public void setSqlId(Integer sqlId) { this.sqlId = sqlId; }

    public int getProductId() { return productId; }
    public void setProductId(int productId) { this.productId = productId; }

    public int getUserId() { return userId; }
    public void setUserId(int userId) { this.userId = userId; }

    public int getRating() { return rating; }
    public void setRating(int rating) { this.rating = rating; }

    public String getComment() { return comment; }
    public void setComment(String comment) { this.comment = comment; }

    public Timestamp getCreatedAt() { return createdAt; }
    public void setCreatedAt(Timestamp createdAt) { this.createdAt = createdAt; }
}
